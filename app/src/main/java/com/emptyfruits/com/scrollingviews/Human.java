package com.emptyfruits.com.scrollingviews;

public class Human {

    private String name, age, email;

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getEmail() {
        return email;
    }

    public Human(String name, String age, String email) {
        this.name = name;
        this.age = age;
        this.email = email;
    }
}
